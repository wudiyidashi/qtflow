use assert_cmd::Command;
use serde_json::Value;

fn clean_qtflow() -> Command {
    let mut command = Command::cargo_bin("qtflow").expect("binary");
    command
        .env_remove("QTFLOW_CONFIG")
        .env_remove("QTFLOW_PROFILE")
        .env_remove("QTFLOW_CMAKE")
        .env_remove("QTFLOW_CTEST")
        .env_remove("QTFLOW_QMAKE")
        .env_remove("QTFLOW_VSDEVCMD_BAT")
        .env_remove("VSDEVCMD_BAT");
    command
}

fn fixture_repo() -> tempfile::TempDir {
    let temp = tempfile::tempdir().expect("tempdir");
    std::fs::write(
        temp.path().join("CMakeLists.txt"),
        "cmake_minimum_required(VERSION 3.20)\n",
    )
    .expect("write CMakeLists");
    temp
}

fn empty_dir() -> tempfile::TempDir {
    tempfile::tempdir().expect("tempdir")
}

fn write_cache(root: &std::path::Path, dir: &str, build_type: &str, generator: &str) {
    write_cache_with_prefix(root, dir, build_type, generator, "");
}

fn write_vs_cache(root: &std::path::Path, dir: &str, build_type: &str, generator: &str) {
    write_cache_with_prefix(
        root,
        dir,
        build_type,
        generator,
        "# It was generated by CMake: C:/Program Files/Microsoft Visual Studio/18/Community/Common7/IDE/CommonExtensions/Microsoft/CMake/CMake/bin/cmake.exe\n",
    );
}

fn write_cache_with_prefix(
    root: &std::path::Path,
    dir: &str,
    build_type: &str,
    generator: &str,
    prefix: &str,
) {
    let build_dir = root.join(dir);
    std::fs::create_dir_all(&build_dir).expect("build dir");
    let home = root.canonicalize().expect("canonical root");
    std::fs::write(
        build_dir.join("CMakeCache.txt"),
        format!(
            "{prefix}CMAKE_BUILD_TYPE:STRING={build_type}\nCMAKE_GENERATOR:INTERNAL={generator}\nCMAKE_HOME_DIRECTORY:INTERNAL={}\n",
            home.display()
        ),
    )
    .expect("write cache");
}

#[test]
fn init_json_creates_detected_claude_skill_and_config_then_rerun_skips() {
    let temp = fixture_repo();
    std::fs::create_dir(temp.path().join(".claude")).expect("create .claude");
    std::fs::write(
        temp.path().join("CMakePresets.json"),
        r#"{
            "version": 6,
            "configurePresets": [
                {"name": "MyDebug", "binaryDir": "${sourceDir}/out/build/${presetName}", "cacheVariables": {"CMAKE_BUILD_TYPE": "Debug"}},
                {"name": "MyRelease", "binaryDir": "${sourceDir}/out/build/${presetName}", "cacheVariables": {"CMAKE_BUILD_TYPE": "Release"}}
            ]
        }"#,
    )
    .expect("write presets");

    let output = clean_qtflow()
        .args(["--project", temp.path().to_str().unwrap()])
        .args(["init", "--json"])
        .assert()
        .success()
        .get_output()
        .stdout
        .clone();
    let json: Value = serde_json::from_slice(&output).expect("valid JSON");

    let skill = temp
        .path()
        .join(".claude/skills/qtflow-build-test/SKILL.md");
    let config = temp.path().join(".qtflow.toml");
    assert!(skill.is_file());
    assert!(config.is_file());
    let actual_root = std::path::Path::new(json["projectRoot"].as_str().expect("projectRoot"))
        .canonicalize()
        .expect("canonical actual root");
    assert_eq!(
        actual_root,
        temp.path().canonicalize().expect("canonical temp")
    );
    assert_eq!(json["actions"].as_array().expect("actions").len(), 2);
    assert!(json["actions"].as_array().unwrap().iter().all(|action| {
        action["status"] == "created" && !action["path"].as_str().unwrap().contains('\\')
    }));

    let skill_text = std::fs::read_to_string(&skill).expect("skill");
    assert!(skill_text.starts_with("---\nname: qtflow-build-test"));
    assert!(skill_text.contains("qtflow check <test-target>"));

    let config_text = std::fs::read_to_string(&config).expect("config");
    assert!(config_text.contains(r#"preset = "MyDebug""#));
    assert!(config_text.contains(r#"preset = "MyRelease""#));
    assert!(config_text.contains(r#"build_dir = "out/build/MyDebug""#));

    let rerun = clean_qtflow()
        .args(["--project", temp.path().to_str().unwrap()])
        .args(["init", "--json"])
        .assert()
        .success()
        .get_output()
        .stdout
        .clone();
    let rerun_json: Value = serde_json::from_slice(&rerun).expect("valid JSON");
    assert!(rerun_json["actions"]
        .as_array()
        .unwrap()
        .iter()
        .all(|action| action["status"] == "skipped"));
}

#[test]
fn init_global_json_without_project_creates_codex_skill_and_rerun_skips() {
    let cwd = empty_dir();
    let codex_home = empty_dir();

    let output = clean_qtflow()
        .current_dir(cwd.path())
        .env("CODEX_HOME", codex_home.path())
        .args(["--project", cwd.path().to_str().unwrap()])
        .args(["init", "--global", "--json"])
        .assert()
        .success()
        .get_output()
        .stdout
        .clone();
    let json: Value = serde_json::from_slice(&output).expect("valid JSON");

    let skill = codex_home.path().join("skills/qtflow-build-test/SKILL.md");
    let metadata = codex_home
        .path()
        .join("skills/qtflow-build-test/agents/openai.yaml");
    assert!(skill.is_file());
    assert!(metadata.is_file());
    assert_eq!(json["actions"].as_array().expect("actions").len(), 2);
    assert!(json["actions"].as_array().unwrap().iter().all(|action| {
        action["kind"] == "skill"
            && action["agent"] == "codex-global"
            && action["status"] == "created"
            && !action["path"].as_str().unwrap().contains('\\')
    }));
    assert!(json["warnings"][0]
        .as_str()
        .expect("warning")
        .contains("skipped repo-scoped init actions"));
    assert!(std::fs::read_to_string(&skill)
        .expect("skill")
        .starts_with("---\nname: qtflow-build-test"));
    assert_eq!(
        std::fs::read_to_string(&metadata).expect("metadata"),
        "interface:\n  display_name: \"QtFlow Build Test\"\n  short_description: \"Build and test Qt/CMake projects through qtflow.\"\n  default_prompt: \"Build and run the focused test for this change with: qtflow check <target>.\"\n"
    );

    let rerun = clean_qtflow()
        .current_dir(cwd.path())
        .env("CODEX_HOME", codex_home.path())
        .args(["--project", cwd.path().to_str().unwrap()])
        .args(["init", "--global", "--json"])
        .assert()
        .success()
        .get_output()
        .stdout
        .clone();
    let rerun_json: Value = serde_json::from_slice(&rerun).expect("valid JSON");
    assert!(rerun_json["actions"]
        .as_array()
        .unwrap()
        .iter()
        .all(|action| action["status"] == "skipped"));
}

