#[test]
fn init_agent_claude_force_overwrites_existing_skill_and_config() {
    let temp = fixture_repo();

    clean_qtflow()
        .args(["--project", temp.path().to_str().unwrap()])
        .args(["init", "--agent", "claude"])
        .assert()
        .success();

    let skill = temp
        .path()
        .join(".claude/skills/qtflow-build-test/SKILL.md");
    let config = temp.path().join(".qtflow.toml");
    std::fs::write(&skill, "custom skill\n").expect("overwrite skill fixture");
    std::fs::write(&config, "custom config\n").expect("overwrite config fixture");

    let skipped = clean_qtflow()
        .args(["--project", temp.path().to_str().unwrap()])
        .args(["init", "--agent", "claude", "--json"])
        .assert()
        .success()
        .get_output()
        .stdout
        .clone();
    let skipped_json: Value = serde_json::from_slice(&skipped).expect("valid JSON");
    assert!(skipped_json["actions"]
        .as_array()
        .unwrap()
        .iter()
        .all(|action| action["status"] == "skipped"));
    assert_eq!(
        std::fs::read_to_string(&skill).expect("skill"),
        "custom skill\n"
    );

    let forced = clean_qtflow()
        .args(["--project", temp.path().to_str().unwrap()])
        .args(["init", "--agent", "claude", "--force", "--json"])
        .assert()
        .success()
        .get_output()
        .stdout
        .clone();
    let forced_json: Value = serde_json::from_slice(&forced).expect("valid JSON");
    assert!(forced_json["actions"]
        .as_array()
        .unwrap()
        .iter()
        .all(|action| action["status"] == "overwritten"));
    assert!(std::fs::read_to_string(&skill)
        .expect("skill")
        .contains("name: qtflow-build-test"));
    assert!(std::fs::read_to_string(&config)
        .expect("config")
        .contains(r#"default_profile = "debug""#));
}
